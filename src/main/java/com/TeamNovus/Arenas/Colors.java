package com.TeamNovus.Arenas;

import org.bukkit.ChatColor;

public class Colors {
	
	/**
	 * To be used as a title, heading etc.
	 */
	public static ChatColor TITLE 		= ChatColor.DARK_GREEN;
	
	/**
	 * To be used as plain text.
	 */
	public static ChatColor TEXT 		= ChatColor.GREEN;
	
	/**
	 * To be used to draw emphasis to a specific word(s).
	 */
	public static ChatColor HIGHLIGHT 	= ChatColor.AQUA;
	
	/**
	 * To be used in successful actions.
	 */
	public static ChatColor SUCCESS 	= ChatColor.DARK_AQUA;
	
	/**
	 * To be used when there is a minor warning.
	 */
	public static ChatColor WARNING		= ChatColor.YELLOW;	
	
	/**
	 * To be used when there is an error.
	 */
	public static ChatColor ERROR 		= ChatColor.RED;
	
	/**
	 * To be used as an extra color code.  Specifically for non-text elements.
	 */
	public static ChatColor EXTRA 		= ChatColor.DARK_RED;
}
